    public void adicionaLinhasTabela(){
        Jogador j = new Jogador();
        DefaultTableModel modelo = (DefaultTableModel) tabelaRanking.getModel(); 
        ArrayList<Jogador> jogadores = j.getJogadores();
        Object rowData[] = new Object[3];
        for (int i = 0; i < jogadores.size(); i++) {
            rowData[0] = jogadores.get(i).getNome();
            rowData[1] = jogadores.get(i).getPontuacao();
            rowData[2] = jogadores.get(i).getData();
            modelo.addRow(rowData);
        }
    }
    
